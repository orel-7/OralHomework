B=0  
C=0 
for i in range(10):
    A=int(input("הכנס את מספר הבנים: "))
    D=int(input("הכנס את מספר הבנות: "))
    E=A+D
    print("סה״כ ילדים במשפחה:", E)

    if A>D:
        B+=1
    if A==0:
        C+=1

print("לכמה משפחות יש יותר בנים מבנות:", B)
print("לכמה משפחות אין בנים:", C)




